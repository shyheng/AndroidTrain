package com.shy.smartcity.Bean;

public class Order {
    private String id;
    private String type;
    private String date;

    public Order(String id, String type, String date) {
        this.id = id;
        this.type = type;
        this.date = date;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
